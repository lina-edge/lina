def utc_now() -> datetime:
    return datetime.now(timezone.utc)


def iso_z(value: datetime) -> str:
    return value.astimezone(timezone.utc).isoformat(timespec='milliseconds').replace('+00:00', 'Z')


def sanitize_name(value: str) -> str:
    cleaned = []
    for char in value:
        if char.isalnum() or char in ('-', '_', '.'):
            cleaned.append(char)
        else:
            cleaned.append('-')
    return ''.join(cleaned).strip('-') or 'experiment'


def parse_key_value(raw: str) -> tuple[str, str]:
    if '=' not in raw:
        raise argparse.ArgumentTypeError(f"expected KEY=VALUE, got '{raw}'")
    key, value = raw.split('=', 1)
    key = key.strip()
    if not key:
        raise argparse.ArgumentTypeError(f"expected KEY=VALUE, got '{raw}'")
    return key, value


def path_from_project(project_root: Path, raw: str) -> Path:
    path = Path(raw).expanduser()
    if path.is_absolute():
        return path
    return project_root / path


def host_for_url(host: str) -> str:
    if ':' in host and not host.startswith('['):
        return f'[{host}]'
    return host


def command_text(command: Iterable[str], env: Optional[dict[str, str]] = None) -> str:
    prefix = ''
    if env:
        prefix = ' '.join(f'{key}={shlex.quote(value)}' for key, value in sorted(env.items())) + ' '
    return prefix + shlex.join(list(command))


def write_json(path: Path, data: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, 'w') as file:
        json.dump(data, file, indent=2)
        file.write('\n')


