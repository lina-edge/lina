def sleep_with_progress(seconds: int, label: str, dry_run: bool) -> None:
    if seconds <= 0:
        return
    if dry_run:
        print(f"[dry-run] would wait {seconds}s for {label}")
        return

    print(f"Waiting {seconds}s for {label}...")
    remaining = seconds
    while remaining > 0:
        step = min(15, remaining)
        time.sleep(step)
        remaining -= step
        if remaining > 0:
            print(f"  {remaining}s remaining")


def run_command(
    command: list[str],
    *,
    cwd: Path,
    dry_run: bool,
    env: Optional[dict[str, str]] = None,
) -> int:
    printable_env = env if env else None
    print(f"$ {command_text(command, printable_env)}")
    if dry_run:
        return 0

    completed = subprocess.run(
        command,
        cwd=str(cwd),
        env={**os.environ, **env} if env else None,
        check=True,
    )
    return completed.returncode


def capture_command(command: list[str], *, cwd: Path, output_file) -> int:
    print(f"$ {command_text(command)}")
    output_file.write(f"$ {command_text(command)}\n")
    completed = subprocess.run(
        command,
        cwd=str(cwd),
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        check=False,
    )
    output_file.write(completed.stdout or '')
    output_file.write(f"\n[exit {completed.returncode}]\n\n")
    return completed.returncode


def run_streamed_command(
    command: list[str],
    *,
    cwd: Path,
    dry_run: bool,
    env: dict[str, str],
    log_path: Path,
) -> int:
    print(f"$ {command_text(command, env)}")
    if dry_run:
        return 0

    log_path.parent.mkdir(parents=True, exist_ok=True)
    with open(log_path, 'w') as log_file:
        process = subprocess.Popen(
            command,
            cwd=str(cwd),
            env={**os.environ, **env},
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
            bufsize=1,
        )
        try:
            assert process.stdout is not None
            for line in process.stdout:
                print(line, end='')
                log_file.write(line)
            return process.wait()
        except KeyboardInterrupt:
            process.terminate()
            try:
                process.wait(timeout=10)
            except subprocess.TimeoutExpired:
                process.kill()
                process.wait()
            raise


def ssh_destination(user: str, host: str) -> str:
    return f'{user}@{host}' if user else host


def remote_compose_command(destination: str, target_dir: str, compose_file: str, action: list[str]) -> list[str]:
    remote = (
        f"cd {shlex.quote(target_dir)} && "
        f"docker compose -f {shlex.quote(compose_file)} {shlex.join(action)}"
    )
    return ['ssh', destination, remote]


def remote_service_log_head_command(
    destination: str,
    target_dir: str,
    compose_file: str,
    service: str,
    head_lines: int,
) -> list[str]:
    remote = (
        f"cd {shlex.quote(target_dir)} && "
        f"docker compose -f {shlex.quote(compose_file)} logs --no-color {shlex.quote(service)} | "
        f"head -n {head_lines}"
    )
    return ['ssh', destination, remote]


def remote_service_log_command(
    destination: str,
    target_dir: str,
    compose_file: str,
    service: str,
    tail_lines: int,
) -> list[str]:
    return remote_compose_command(
        destination,
        target_dir,
        compose_file,
        ['logs', '--no-color', f'--tail={tail_lines}', service],
    )


def validate_remote_data_dir(raw: str) -> str:
    data_dir = raw.strip()
    if not data_dir:
        raise ValueError('--target-data-dir must not be empty')

    parts = [part for part in data_dir.split('/') if part]
    if '..' in parts:
        raise ValueError(f"--target-data-dir must not contain '..': {raw}")

    normalized = posixpath.normpath(data_dir)
    if normalized in ('.', '/', '..'):
        raise ValueError(f'--target-data-dir is too broad to reset safely: {raw}')

    return normalized


def remote_reset_data_command(
    destination: str,
    target_dir: str,
    data_dir: str,
    *,
    use_sudo: bool,
) -> list[str]:
    data_root = validate_remote_data_dir(data_dir)
    subdirs = [posixpath.join(data_root, subdir) for subdir in EDGE_DATA_SUBDIRS]
    mkdir_paths = ' '.join(shlex.quote(path) for path in [data_root, *subdirs])
    remove_command = ['rm', '-rf', '--', data_root]
    if use_sudo:
        remove_command = ['sudo', '-n', *remove_command]
    remote = (
        f"cd {shlex.quote(target_dir)} && "
        f"{shlex.join(remove_command)} && "
        f"mkdir -p -- {mkdir_paths}"
    )
    return ['ssh', destination, remote]


def collect_target_diagnostics(args: argparse.Namespace, project_root: Path, run_dir: Path) -> Path:
    destination = ssh_destination(args.target_user, args.target_host)
    diagnostics_path = run_dir / 'target_diagnostics.log'
    commands = [
        remote_compose_command(destination, args.target_dir, args.target_compose_file, ['ps', '--all']),
        remote_compose_command(destination, args.target_dir, args.target_compose_file, ['logs', '--tail=200', 'redis']),
        ['ssh', destination, 'docker ps -a --format "table {{.Names}}\\t{{.Status}}\\t{{.Ports}}"'],
        ['ssh', destination, 'docker network ls --format "table {{.Name}}\\t{{.Driver}}"'],
        ['ssh', destination, 'docker network inspect deployment_default --format "{{json .Containers}}"'],
        ['ssh', destination, 'docker inspect redis --format "{{json .State}}"'],
        ['ssh', destination, 'docker inspect redis --format "{{json .Config.Image}} {{json .Config.Entrypoint}} {{json .Config.Cmd}}"'],
        ['ssh', destination, 'docker inspect postgres --format "{{json .Config.Labels}}"'],
        ['ssh', destination, 'uname -a'],
        ['ssh', destination, 'lscpu'],
    ]

    print(f"Collecting target diagnostics into {diagnostics_path}")
    diagnostics_path.parent.mkdir(parents=True, exist_ok=True)
    with open(diagnostics_path, 'w') as output_file:
        output_file.write(f"target={destination}\n")
        output_file.write(f"collected_at={iso_z(utc_now())}\n\n")
        for command in commands:
            capture_command(command, cwd=project_root, output_file=output_file)
    return diagnostics_path


def capture_target_service_logs(args: argparse.Namespace, project_root: Path, run_dir: Path) -> dict[str, str]:
    destination = ssh_destination(args.target_user, args.target_host)
    logs_dir = run_dir / 'service_logs'
    logs_dir.mkdir(parents=True, exist_ok=True)
    captured_logs = {}

    for service in TARGET_SERVICE_LOGS:
        log_path = logs_dir / f'{service}.log'
        head_command = remote_service_log_head_command(
            destination,
            args.target_dir,
            args.target_compose_file,
            service,
            args.service_log_head_lines,
        )
        tail_command = remote_service_log_command(
            destination,
            args.target_dir,
            args.target_compose_file,
            service,
            args.service_log_tail_lines,
        )
        captured_logs[service] = str(log_path)
        print(f"Capturing {service} logs into {log_path}")
        if args.dry_run:
            print(f"[dry-run] would capture first lines: {command_text(head_command)}")
            print(f"[dry-run] would capture tail lines: {command_text(tail_command)}")
            continue
        with open(log_path, 'w') as output_file:
            output_file.write(f"service={service}\n")
            output_file.write(f"target={destination}\n")
            output_file.write(f"collected_at={iso_z(utc_now())}\n\n")
            output_file.write(f"== first {args.service_log_head_lines} lines ==\n")
            capture_command(head_command, cwd=project_root, output_file=output_file)
            output_file.write(f"== tail {args.service_log_tail_lines} lines ==\n")
            capture_command(tail_command, cwd=project_root, output_file=output_file)

    return captured_logs


