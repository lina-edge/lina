def build_k6_command(args: argparse.Namespace, loadtest_path: Path) -> list[str]:
    command = [args.k6_bin, 'run', '-o', args.k6_output]
    if args.insecure_skip_tls_verify:
        command.append('--insecure-skip-tls-verify')
    command.extend(args.k6_arg or [])
    command.append(str(loadtest_path))
    return command


def build_export_command(
    args: argparse.Namespace,
    project_root: Path,
    export_script: Path,
    metrics_dir: Path,
    export_from: str,
    export_to: str,
    export_vars: list[tuple[str, str]],
) -> list[str]:
    command = [
        args.python,
        str(export_script),
        '--from',
        export_from,
        '--to',
        export_to,
        '--prometheus-url',
        args.prometheus_url,
        '--output-dir',
        str(metrics_dir),
        '--dashboard',
        str(Path(args.dashboard) if Path(args.dashboard).is_absolute() else Path(args.dashboard)),
        '--step',
        args.export_step,
    ]
    for key, value in export_vars:
        command.extend(['--var', f'{key}={value}'])
    return command


