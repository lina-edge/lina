def main() -> int:
    args = parse_args()
    project_root = Path(__file__).resolve().parents[2]
    run_name = sanitize_name(args.run_name or default_run_name(args.target_host))
    results_root = path_from_project(project_root, args.results_root)
    run_dir = results_root / run_name
    metrics_dir = run_dir / 'metrics_export'
    graphs_dir = run_dir / 'graphs'
    manifest_path = run_dir / 'experiment.json'
    override_file = run_dir / 'external-target.override.yml'
    k6_log = run_dir / 'k6.log'
    service_logs_dir = run_dir / 'service_logs'

    local_compose_file = path_from_project(project_root, args.local_compose_file)
    loadtest_path = path_from_project(project_root, args.loadtest_file)
    export_script = path_from_project(project_root, args.export_script)
    plot_script = path_from_project(project_root, args.plot_script)

    api_base_url = args.api_base_url or f"http://{host_for_url(args.target_host)}:{args.api_port}"
    mqtt_tls_server_name = args.mqtt_tls_server_name or args.target_host
    target_instance = args.target_metrics_instance or f'{args.target_host}:9463'
    export_vars = list(args.export_var or [])
    if not args.no_target_instance_var and not any(key == 'instance' for key, _value in export_vars):
        export_vars.append(('instance', target_instance))
    should_reset_target_data = not args.skip_target_restart and not args.skip_target_data_reset
    target_data_dir = validate_remote_data_dir(args.target_data_dir) if should_reset_target_data else None

    k6_env = {
        'API_BASE_URL': api_base_url,
        'HTTPDEVICE_BASE_URL': args.httpdevice_base_url,
        'K6_PROMETHEUS_RW_SERVER_URL': args.prometheus_rw_url,
        'LEVEL_VUS': str(args.level_vus),
        'LEVEL_COUNT': str(args.level_count),
        'IDLE': args.idle,
        'WARMUP': args.warmup,
        'MEASURE': args.measure,
        'TEARDOWN': args.teardown,
        'USAGE_REPORT_INTERVAL': str(args.usage_report_interval),
        'LOG_LEVEL': args.log_level,
    }
    for key, value in args.k6_env or []:
        k6_env[key] = value

    manifest = {
        'run_name': run_name,
        'created_at': iso_z(utc_now()),
        'target': {
            'host': args.target_host,
            'user': args.target_user,
            'ssh_destination': ssh_destination(args.target_user, args.target_host),
            'dir': args.target_dir,
            'compose_file': args.target_compose_file,
            'data_dir': target_data_dir,
            'data_reset_enabled': should_reset_target_data,
            'data_reset_sudo': should_reset_target_data and not args.no_target_data_reset_sudo,
            'api_base_url': api_base_url,
            'metrics_instance': None if args.no_target_instance_var else target_instance,
        },
        'local': {
            'project_root': str(project_root),
            'compose_file': str(local_compose_file),
            'compose_override_file': str(override_file),
            'prometheus_url': args.prometheus_url,
            'prometheus_rw_url': args.prometheus_rw_url,
            'mqtt_tls_server_name': mqtt_tls_server_name,
        },
        'outputs': {
            'run_dir': str(run_dir),
            'k6_log': str(k6_log),
            'service_logs_dir': str(service_logs_dir),
            'metrics_dir': str(metrics_dir),
            'graphs_dir': str(graphs_dir),
            'target_diagnostics': str(run_dir / 'target_diagnostics.log'),
        },
        'k6': {
            'env': k6_env,
            'return_code': None,
            'started_at': None,
            'finished_at': None,
        },
        'service_logs': {
            'services': list(TARGET_SERVICE_LOGS),
            'head_lines': args.service_log_head_lines,
            'tail_lines': args.service_log_tail_lines,
            'captured_at': None,
            'files': {},
        },
        'export': {
            'time_range': None,
            'variables': {key: value for key, value in export_vars},
            'step': args.export_step,
        },
        'commands': {},
        'status': 'running' if not args.dry_run else 'dry-run',
    }

    run_dir.mkdir(parents=True, exist_ok=True)
    write_compose_override(override_file, args.target_host, mqtt_tls_server_name)
    write_json(manifest_path, manifest)

    print(f"Run directory: {run_dir}")
    print(f"Target API: {api_base_url}")
    print(f"Prometheus export variable: instance={target_instance}" if not args.no_target_instance_var else "Prometheus instance variable disabled")

    try:
        destination = ssh_destination(args.target_user, args.target_host)
        if not args.skip_target_restart:
            down_cmd = remote_compose_command(
                destination,
                args.target_dir,
                args.target_compose_file,
                ['down', '-v', '--remove-orphans'],
            )
            pull_cmd = remote_compose_command(destination, args.target_dir, args.target_compose_file, ['pull'])
            up_cmd = remote_compose_command(
                destination,
                args.target_dir,
                args.target_compose_file,
                ['up', '-d', '--remove-orphans'],
            )
            manifest['commands']['target_down'] = command_text(down_cmd)
            reset_data_cmd = None
            if target_data_dir is not None:
                reset_data_cmd = remote_reset_data_command(
                    destination,
                    args.target_dir,
                    target_data_dir,
                    use_sudo=not args.no_target_data_reset_sudo,
                )
                manifest['commands']['target_data_reset'] = command_text(reset_data_cmd)
            if args.pull_target_images:
                manifest['commands']['target_pull'] = command_text(pull_cmd)
            manifest['commands']['target_up'] = command_text(up_cmd)
            write_json(manifest_path, manifest)
            run_command(down_cmd, cwd=project_root, dry_run=args.dry_run)
            if reset_data_cmd is not None:
                run_command(reset_data_cmd, cwd=project_root, dry_run=args.dry_run)
                manifest['target']['data_reset_at'] = iso_z(utc_now())
                write_json(manifest_path, manifest)
            if args.pull_target_images:
                run_command(pull_cmd, cwd=project_root, dry_run=args.dry_run)
            try:
                run_command(up_cmd, cwd=project_root, dry_run=args.dry_run)
            except subprocess.CalledProcessError:
                if not args.dry_run:
                    collect_target_diagnostics(args, project_root, run_dir)
                raise
            manifest['target']['restarted_at'] = iso_z(utc_now())
            write_json(manifest_path, manifest)
            sleep_with_progress(args.target_stabilize_seconds, 'target edge stack stabilization', args.dry_run)

        if not args.skip_local_restart:
            down_cmd = local_compose_command(project_root, local_compose_file, override_file, ['down', '-v', '--remove-orphans'])
            up_cmd = local_compose_command(project_root, local_compose_file, override_file, ['up', '-d', '--remove-orphans'])
            manifest['commands']['local_down'] = command_text(down_cmd)
            manifest['commands']['local_up'] = command_text(up_cmd)
            write_json(manifest_path, manifest)
            run_command(down_cmd, cwd=project_root, dry_run=args.dry_run)
            run_command(up_cmd, cwd=project_root, dry_run=args.dry_run)
            manifest['local']['restarted_at'] = iso_z(utc_now())
            write_json(manifest_path, manifest)
            sleep_with_progress(args.local_stabilize_seconds, 'local external stack stabilization', args.dry_run)

        k6_cmd = build_k6_command(args, loadtest_path)
        manifest['commands']['k6'] = command_text(k6_cmd, k6_env)
        manifest['k6']['started_at'] = iso_z(utc_now())
        write_json(manifest_path, manifest)

        k6_started_at = utc_now()
        manifest['k6']['started_at'] = iso_z(k6_started_at)
        write_json(manifest_path, manifest)
        print(f"k6 started at {manifest['k6']['started_at']}")
        if shutil.which(args.k6_bin) is None and not args.dry_run:
            raise RuntimeError(f"k6 executable not found: {args.k6_bin}")
        k6_return_code = run_streamed_command(
            k6_cmd,
            cwd=project_root,
            dry_run=args.dry_run,
            env=k6_env,
            log_path=k6_log,
        )
        k6_finished_at = utc_now()
        manifest['k6']['finished_at'] = iso_z(k6_finished_at)
        manifest['k6']['return_code'] = k6_return_code
        write_json(manifest_path, manifest)
        print(f"k6 finished at {manifest['k6']['finished_at']} with exit code {k6_return_code}")

        service_log_commands = {}
        destination = ssh_destination(args.target_user, args.target_host)
        for service in TARGET_SERVICE_LOGS:
            service_log_commands[service] = {
                'head': command_text(
                    remote_service_log_head_command(
                        destination,
                        args.target_dir,
                        args.target_compose_file,
                        service,
                        args.service_log_head_lines,
                    )
                ),
                'tail': command_text(
                    remote_service_log_command(
                        destination,
                        args.target_dir,
                        args.target_compose_file,
                        service,
                        args.service_log_tail_lines,
                    )
                ),
            }
        manifest['commands']['service_logs'] = service_log_commands
        manifest['service_logs']['files'] = capture_target_service_logs(args, project_root, run_dir)
        manifest['service_logs']['captured_at'] = iso_z(utc_now())
        write_json(manifest_path, manifest)

        export_from_dt = k6_started_at - timedelta(seconds=args.export_padding_before_seconds)
        export_to_dt = k6_finished_at + timedelta(seconds=args.export_padding_after_seconds)
        export_from = iso_z(export_from_dt)
        export_to = iso_z(export_to_dt)
        manifest['export']['time_range'] = {'from': export_from, 'to': export_to}
        write_json(manifest_path, manifest)

        print('Metric export window:')
        print(json.dumps({'from': export_from, 'to': export_to}))

        if k6_return_code != 0 and args.stop_on_k6_failure:
            manifest['status'] = 'k6_failed'
            write_json(manifest_path, manifest)
            return k6_return_code

        sleep_with_progress(args.post_k6_settle_seconds, 'k6 remote-write flush', args.dry_run)

        if not args.skip_export:
            export_cmd = build_export_command(
                args,
                project_root,
                export_script,
                metrics_dir,
                export_from,
                export_to,
                export_vars,
            )
            manifest['commands']['export'] = command_text(export_cmd)
            write_json(manifest_path, manifest)
            run_command(export_cmd, cwd=project_root, dry_run=args.dry_run)

        if not args.skip_plot:
            plot_cmd = build_plot_command(args, plot_script, metrics_dir, graphs_dir)
            manifest['commands']['plot'] = command_text(plot_cmd)
            write_json(manifest_path, manifest)
            run_command(plot_cmd, cwd=project_root, dry_run=args.dry_run)

        manifest['status'] = 'completed' if k6_return_code == 0 else 'completed_with_k6_failure'
        write_json(manifest_path, manifest)
        print(f"Experiment manifest: {manifest_path}")
        return k6_return_code

    except KeyboardInterrupt:
        manifest['status'] = 'interrupted'
        manifest['interrupted_at'] = iso_z(utc_now())
        write_json(manifest_path, manifest)
        print('\nInterrupted. Manifest updated with the partial run state.', file=sys.stderr)
        return 130
    except Exception as exc:
        manifest['status'] = 'failed'
        manifest['failed_at'] = iso_z(utc_now())
        manifest['error'] = str(exc)
        write_json(manifest_path, manifest)
        print(f"Experiment failed: {exc}", file=sys.stderr)
        return 1


if __name__ == '__main__':
    sys.exit(main())
